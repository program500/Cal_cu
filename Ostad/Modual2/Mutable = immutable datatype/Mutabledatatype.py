#Mutable data type 
#1=list
#2=Dictionary
#3=Sets

#list type data is mutable 
fruits=['apple', 'banana', 'cherry', 'date']
print(id(fruits))
print(id(fruits))

#Mutable dictionary type data 
data = {
        "name": "Hasip",
        "age": 20,
        "city":"Dhaka"
}
print(id(data))
print(id(data))

#mutable data type 
my_sets = {'Onion','garlic','arum','memory'}
print(id(my_sets))
print(id(my_sets))